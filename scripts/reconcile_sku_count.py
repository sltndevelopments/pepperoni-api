ROOT = Path(__file__).parent.parent
PUBLIC = ROOT / "public"
PRODUCTS_JSON = PUBLIC / "products.json"
STATE_FILE = ROOT / "data" / "sku_count_state.json"


def ru_tovar(n: int) -> str:
    n10, n100 = n % 10, n % 100
    if n10 == 1 and n100 != 11:
        return f"{n} товар"
    if 2 <= n10 <= 4 and not (12 <= n100 <= 14):
        return f"{n} товара"
    return f"{n} товаров"


def ru_naimenovanie(n: int) -> str:
    n10, n100 = n % 10, n % 100
    if n10 == 1 and n100 != 11:
        return f"{n} наименование"
    if 2 <= n10 <= 4 and not (12 <= n100 <= 14):
        return f"{n} наименования"
    return f"{n} наименований"


SECTION_LABELS_EN = {
    "Заморозка": "frozen meats",
    "Охлаждённая продукция": "refrigerated/chilled meats",
    "Выпечка": "bakery",
}


def get_live_data() -> dict:
    data = json.loads(PRODUCTS_JSON.read_text(encoding="utf-8"))
    products = data.get("products", [])
    n = data.get("totalProducts")
    if not isinstance(n, int) or n <= 0:
        n = len(products)
    if not n:
        raise SystemExit("reconcile_sku_count: could not determine live SKU count")

    counts = Counter(p.get("section") for p in products)
    frozen = counts.get("Заморозка", 0)
    chilled = counts.get("Охлаждённая продукция", 0)
    bakery = counts.get("Выпечка", 0)

    parts = []
    for ru_name, en_label in SECTION_LABELS_EN.items():
        c = counts.get(ru_name)
        if c:
            parts.append(f"{en_label} ({c} SKUs)")
    if len(parts) > 1:
        sections_en = ", ".join(parts[:-1]) + f", and {parts[-1]}"
    else:
        sections_en = parts[0] if parts else ""

    return {
        "n": n,
        "ru": ru_tovar(n),
        "ru_name": ru_naimenovanie(n),
        "sections_en": sections_en,
        "frozen": frozen,
        "chilled": chilled,
        "bakery": bakery,
        "n8": n * 8,
    }


def render(template: str, ctx: dict) -> str:
    return template.format(**ctx)



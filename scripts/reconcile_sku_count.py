def patch_file(path: Path, rules: list[tuple[str, str]], ctx: dict, check_only: bool) -> tuple[bool, bool]:
    """Returns (would_change_or_changed, wrote)."""
    if not path.exists():
        print(f"  [skip] {path.relative_to(ROOT)} does not exist")
        return False, False
    text = path.read_text(encoding="utf-8")
    original = text
    for pattern, template in rules:
        repl = render(template, ctx)

        def _sub(m: re.Match, _repl=repl) -> str:
            out = _repl
            out = out.replace("\\1", m.group(1) if m.lastindex and m.lastindex >= 1 else "")
            out = out.replace("\\2", m.group(2) if m.lastindex and m.lastindex >= 2 else "")
            for i in range(1, (m.lastindex or 0) + 1):
                out = out.replace(f"\\g<{i}>", m.group(i))
            return out

        if "\\1" in repl or "\\g<" in repl or "\\2" in repl:
            text = re.sub(pattern, _sub, text)
        else:
            text = re.sub(pattern, repl, text)
    # Fix about section headings (any digit → live section count)
    if path.name == "about.html" and path.parent == PUBLIC:
        text = re.sub(
            r"❄️ Заморозка \(\d+ товар(?:а|ов)?\)",
            f"❄️ Заморозка ({ru_tovar(ctx['frozen'])})",
            text,
        )
        text = re.sub(
            r"🧊 Охлаждённая продукция \(\d+ товар(?:а|ов)?\)",
            f"🧊 Охлаждённая продукция ({ru_tovar(ctx['chilled'])})",
            text,
        )
        text = re.sub(
            r"🥐 Выпечка \(\d+ товар(?:а|ов)?\)",
            f"🥐 Выпечка ({ru_tovar(ctx['bakery'])})",
            text,
        )
    if text == original:
        print(f"  [ok] {path.relative_to(ROOT)} already matches ({ctx['n']})")
        return False, False
    if check_only:
        print(f"  [MISMATCH] {path.relative_to(ROOT)}")
        return True, False
    path.write_text(text, encoding="utf-8")
    print(f"  [fixed] {path.relative_to(ROOT)}")
    return True, True


def save_state(n: int) -> None:
    STATE_FILE.parent.mkdir(parents=True, exist_ok=True)
    prev = None
    if STATE_FILE.exists():
        try:
            prev = json.loads(STATE_FILE.read_text(encoding="utf-8")).get("current")
        except Exception:
            prev = None
    STATE_FILE.write_text(
        json.dumps({"previous": prev, "current": n}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )


def main() -> int:
    check_only = "--check" in sys.argv
    ctx = get_live_data()
    print(f"Live SKU count (products.json.totalProducts): {ctx['n']}")
    print(
        f"  sections: frozen={ctx['frozen']} chilled={ctx['chilled']} bakery={ctx['bakery']}"
    )

    any_mismatch = False
    any_changed = False
    for path, rules in TARGETS:
        if not path.exists():
            continue
        mismatched, wrote = patch_file(path, rules, ctx, check_only)
        any_mismatch = any_mismatch or mismatched
        any_changed = any_changed or wrote

    if not check_only:
        save_state(ctx["n"])

    if check_only:
        if any_mismatch:
            print("reconcile_sku_count --check: mismatches found")
            return 1
        print("reconcile_sku_count --check: all files in sync")
        return 0

    if any_changed:
        print("reconcile_sku_count: patched drifted file(s)")
    else:
        print("reconcile_sku_count: nothing to do, all in sync")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

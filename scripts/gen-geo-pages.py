def update_sitemap(slugs: list[str]):
    sitemap_path = PUBLIC / "sitemap.xml"
    content = sitemap_path.read_text(encoding="utf-8")

    # Remove existing geo entries to avoid duplicates
    import re
    content = re.sub(
        r'\s*<url>\s*<loc>https://pepperoni\.tatar/geo/[^<]+</loc>.*?</url>',
        '', content, flags=re.DOTALL
    )

    today = "2026-03-11"
    new_urls = ""
    for slug in slugs:
        new_urls += f"""  <url>
    <loc>https://pepperoni.tatar/geo/{slug}</loc>
    <lastmod>{today}</lastmod>
    <changefreq>monthly</changefreq>
    <priority>0.7</priority>
  </url>
"""

    content = content.replace("</urlset>", new_urls + "</urlset>")
    sitemap_path.write_text(content, encoding="utf-8")
    print(f"✅ sitemap.xml обновлён ({len(slugs)} гео-страниц)")


